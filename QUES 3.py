a = 56
b = 10
# bitwise AND operation
print("a & b =", a & b)

# bitwise OR operation
print("a | b =", a | b)

# bitwise NOT operation
print("~a =", ~a)

# bitwise XOR operation
print("a ^ b =", a ^ b)

# shifting both a and b by 2 bits
print("a << 2 =", a<<2)
print("b << 2 =", b<<2)

#Shifting a and b to right 2 bits and 4 bits respectively
print("a >> 2 =", a>>2)
print("b >> 4 =", b>>4)
